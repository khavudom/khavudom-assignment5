export function updateArr(arr, copyArr) {
  // console.log(arr);
  arr.forEach((arrItem) => {
    const { id, imgSrc, name, price } = arrItem;

    const newItem = {
      id: id,
      imgSrc: imgSrc,
      name: name,
      price: price,
      disPrice: Math.round(price * discountRating, 2),
    };

    copyArr.push(newItem);
  });
}

export function copyArray(arr, arr2) {
  arr.forEach((obj) => {
    obj.forEach((item) => {
      arr2.push(item);
    });
  });
}

export function sum(arr) {
  priceTotal = 0;
  arr.forEach((element) => {
    priceTotal += element.disPrice;
  });
  return priceTotal;
}
